<b><P style="font-size: 25px; font-family: sans-serif;">EMBEDDED C PROGRAMMING LANGUAGE </P></b><br><br>

<P style="font-size: 18px; font-family: sans-serif;">Embedded C is an extension C language, which are used to develop micro-controller based applications (low level and high level).</br>

If we look around the world, we will find various types of Embedded Systems, like mobiles, digital cameras washing machines, AC etc. These all devices are micro-controller/micro-processor based and mostly embedded devices use Embedded C language to develop their device drivers, applications.</br>

Embedded C is an extension of C language (some of the features are there, which can be used to specific purposes), it is used to develop micro-controller based applications (low-level or/and application level).</br>

You must have good knowledge about the hardware for that you’re developing any code. Embedded C is fully hardware dependent language.</br>
The compiler generates some initial code automatically (which may include some assembly language code/files) based on the selected micro-controller/microprocessor.</br>

These functions may not work, because in an embedded device there may not any standard output device (like monitor, Keyboard etc.). you have to write code to display output to connected display unit like 16X2 LCD, graphics display etc.</br>

It’s not too easy to read, understand, modify and fix the bugs in an Embedded C language program. </br>


Keil compiler (An Arm company compilers), BiPOM ELECTRONIC – Embedded training and Development, Green Hill software etc are some of the popular compilers to compile, run an Embedded C language program.</P>

<b><P style="font-size: 25px; font-family: sans-serif;">C PROGRAMMING LANGUAGE</P></b></br>

</br><P style="font-size: 18px; font-family: sans-serif;">C programming language was designed by the Dennis Ritchie in 1972 in Bell Labs. It was developed to rewrite the code of Unix operating system, later it was launched for other/general purposes. C language is a general purpose, structural programming language which follows the concept of POP (procedural oriented programming);it is completely based on procedures/function/modules. </br>

C is a general purpose programming language, which can be used to design any type of desktop based applications.</br>
C language program is hardware independent.</br>
For C language, the standard compilers can be used to compile and execute the program.</br>
In the C programming language, we can use standard function like printf(), scanf() etc for output and input.</br>
Readability modifications, bug fixing are very easy in a C language program. C language compilers generate operating system dependent executable files that can be run on the same operating system.</br>
GCC (GNU Complier collection), Borland turbo C, Intel C++ compiler are some of the popular compilers which are used to compile, execute a C language program.</br>

Initially C programming language was used to rewrite the UNIX operating system, due to its features (like flexibility, easiness, fast, powerful) it is using since 1972 to till date and it is most important and powerful language nowadays.</br></br></br></P>


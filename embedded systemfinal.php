<P style="font-family: sans-serif; font-size: 22px; color: black;"/><B>Applications</B></P>
<BR/><HR/><BR/><BR/>

<P style="font-family: sans-serif; font-size: 20px; color: black;">
Embedded systems are commonly found in consumer, industrial, automotive, medical, commercial and military applications.

Telecommunications systems employ numerous embedded systems from telephone switches for the network to cell phones at the end user. Computer networking uses dedicated routers and network bridges to route data.

Consumer electronics include MP3 players, mobile phones, video game consoles, digital cameras, GPS receivers, and printers. Household appliances, such as microwave ovens, washing machines and dishwashers, include embedded systems to provide flexibility, efficiency and features. Advanced HVAC systems use networked thermostats to more accurately and efficiently control temperature that can change by time of day and season. Home automation uses wired- and wireless-networking that can be used to control lights, climate, security, audio/visual, surveillance, etc., all of which use embedded devices for sensing and controlling. </P>



<P style="font-family: sans-serif; font-size: 20px; color: black;"/>Transportation systems from flight to automobiles increasingly use embedded systems. New airplanes contain advanced avionics such as inertial guidance systems and GPS receivers that also have considerable safety requirements. Various electric motors — brushless DC motors, induction motors and DC motors — use electric/electronic motor controllers. Automobiles, electric vehicles, and hybrid vehicles increasingly use embedded systems to maximize efficiency and reduce pollution. Other automotive safety systems include anti-lock braking system (ABS), Electronic Stability Control (ESC/ESP), traction control (TCS) and automatic four-wheel drive.

Medical equipment uses embedded systems for vital signs monitoring, electronic stethoscopes for amplifying sounds, and various medical imaging (PET, SPECT, CT, and MRI) for non-invasive internal inspections. Embedded systems within medical equipment are often powered by industrial computers.</P><BR/><BR/><BR/>
<P style="font-family: sans-serif; font-size: 22px; color: black;"/><B>Peripherals</B></P><BR/><HR/><BR/><BR/>

<P style="font-family: sans-serif; font-size: 20px; color: black;">
<B>Embedded systems talk with the outside world via peripherals, such as:</B><BR/><BR/>

    Serial Communication Interfaces (SCI): RS-232, RS-422, RS-485, etc.
    Synchronous Serial Communication Interface: I2C, SPI, SSC and ESSI (Enhanced Synchronous Serial Interface).
    Universal Serial Bus (USB).
    Multi Media Cards (SD cards, Compact Flash, etc.).
    Networks: Ethernet, LonWorks, etc.
    Fieldbuses: CAN-Bus, LIN-Bus, PROFIBUS, etc.
    Timers: PLL(s), Capture/Compare and Time Processing Units.
    Discrete IO: aka General Purpose Input/Output (GPIO).
    Analog to Digital/Digital to Analog (ADC/DAC).
    Debugging: JTAG, ISP, BDM Port, BITP, and DB9 ports..
